require("dotenv").config();

const path = require("path");
const fs = require("fs/promises");
const { createClient } = require("@supabase/supabase-js");

const root = path.join(__dirname, "..");
const backupsPath = path.join(root, "backups");
const SUPABASE_URL = process.env.SUPABASE_URL;
const SUPABASE_KEY =
    process.env.SUPABASE_KEY ||
    process.env.SUPABASE_PUBLIC_KEY ||
    process.env.SUPABASE_ANON_KEY ||
    process.env.SUPABASE_PUBLISHABLE_KEY;

const tablas = ["eventos", "fotos", "notas", "lugares"];
const pageSize = 1000;

const schemaSql = `create table if not exists public.eventos (
  id bigint generated by default as identity primary key,
  fecha date,
  hora time,
  lugar text,
  actividad text,
  otros text,
  creado_en timestamptz default now()
);

create table if not exists public.fotos (
  id bigint generated by default as identity primary key,
  url text,
  imagen text,
  fecha date,
  lugar text,
  creado_en timestamptz default now()
);

create table if not exists public.notas (
  id bigint generated by default as identity primary key,
  usuario text,
  mensaje text,
  creado_en timestamptz default now()
);

create table if not exists public.lugares (
  id bigint generated by default as identity primary key,
  nombre text,
  visitado boolean default false,
  creado_en timestamptz default now()
);

insert into storage.buckets (id, name, public)
values ('album', 'album', true)
on conflict (id) do nothing;
`;

if (!SUPABASE_URL || !SUPABASE_KEY) {
    console.error("Falta SUPABASE_URL o SUPABASE_KEY para crear el respaldo.");
    process.exit(1);
}

const supabase = createClient(SUPABASE_URL, SUPABASE_KEY, {
    auth: {
        persistSession: false,
        autoRefreshToken: false
    }
});

function timestamp() {
    return new Date()
        .toISOString()
        .replace(/[:.]/g, "-")
        .replace("T", "_")
        .replace("Z", "");
}

function nombreSeguro(nombre) {
    return String(nombre || "archivo")
        .toLowerCase()
        .replace(/\s+/g, "-")
        .replace(/[^a-z0-9.\-_]/g, "")
        .slice(0, 120);
}

function nombreDesdeUrl(url, id) {
    try {
        const pathname = new URL(url).pathname;
        const base = nombreSeguro(path.basename(decodeURIComponent(pathname)));
        return `${id || Date.now()}-${base || "foto.jpg"}`;
    } catch (error) {
        return `${id || Date.now()}-foto.jpg`;
    }
}

async function consultarTabla(tabla) {
    const registros = [];
    let inicio = 0;

    while (true) {
        const fin = inicio + pageSize - 1;
        const { data, error } = await supabase
            .from(tabla)
            .select("*")
            .order("id", { ascending: true })
            .range(inicio, fin);

        if (error) throw error;

        registros.push(...(data || []));

        if (!data || data.length < pageSize) break;
        inicio += pageSize;
    }

    return registros;
}

async function guardarJson(destino, nombre, datos) {
    await fs.writeFile(
        path.join(destino, nombre),
        JSON.stringify(datos, null, 2),
        "utf8"
    );
}

async function descargarFoto(foto, fotosPath) {
    const url = foto.url || foto.imagen || "";

    if (!url || !url.startsWith("http")) {
        return {
            id: foto.id,
            url,
            descargada: false,
            razon: "Sin URL publica"
        };
    }

    try {
        const respuesta = await fetch(url);

        if (!respuesta.ok) {
            throw new Error(`HTTP ${respuesta.status}`);
        }

        const nombre = nombreDesdeUrl(url, foto.id);
        const archivo = path.join(fotosPath, nombre);
        const buffer = Buffer.from(await respuesta.arrayBuffer());

        await fs.writeFile(archivo, buffer);

        return {
            id: foto.id,
            url,
            descargada: true,
            archivo: `fotos/${nombre}`
        };
    } catch (error) {
        return {
            id: foto.id,
            url,
            descargada: false,
            razon: error.message
        };
    }
}

async function main() {
    const nombre = `supabase-${timestamp()}`;
    const destino = path.join(backupsPath, nombre);
    const fotosPath = path.join(destino, "fotos");
    const datos = {};

    await fs.mkdir(destino, { recursive: true });
    await fs.mkdir(fotosPath, { recursive: true });

    for (const tabla of tablas) {
        datos[tabla] = await consultarTabla(tabla);
        await guardarJson(destino, `${tabla}.json`, datos[tabla]);
    }

    const fotosDescargadas = [];

    for (const foto of datos.fotos || []) {
        fotosDescargadas.push(await descargarFoto(foto, fotosPath));
    }

    const manifest = {
        creado_en: new Date().toISOString(),
        proyecto: SUPABASE_URL,
        tablas: Object.fromEntries(tablas.map(tabla => [tabla, datos[tabla].length])),
        fotos_descargadas: fotosDescargadas.filter(foto => foto.descargada).length,
        fotos_fallidas: fotosDescargadas.filter(foto => !foto.descargada).length
    };

    await guardarJson(destino, "agenda-supabase.json", datos);
    await guardarJson(destino, "fotos-manifest.json", fotosDescargadas);
    await guardarJson(destino, "manifest.json", manifest);
    await fs.writeFile(path.join(destino, "schema.sql"), schemaSql, "utf8");

    console.log(`Respaldo Supabase creado: ${destino}`);
    console.log(JSON.stringify(manifest, null, 2));
}

main().catch(error => {
    console.error("No se pudo crear el respaldo de Supabase:", error.message);
    process.exit(1);
});
